
;<Program title>

jmp start

;data


;code
start: nop
LDA 0000H
CPI 01H
JZ LABEL1
CPI 02H
JZ LABEL2
CPI 03H
JZ LABEL3
LABEL1: CALL ADDTION
JMP FINISH
LABEL2: CALL SUBTRA
JMP FINISH
LABEL3: CALL MULTIPLY

FINISH: hlt

;ADD
ADDTION: LHLD 0002H
MVI C,00H
MOV A,H
ADD L
JNC CARRY
INR C
CARRY: STA 000AH
MOV A,C
STA 000BH
RET

SUBTRA: MVI C,00H
LHLD 0002H 
MOV A,H
SUB L
JNC LABEL
INR C
LABEL: STA 000AH
MOV A,C
STA 000BH
RET

MULTIPLY: LHLD 0002H
MOV C,H
MVI A,00H
LOOP: ADD L
DCR C
JNZ LOOP
STA 000AH
RET















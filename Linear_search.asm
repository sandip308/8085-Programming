
;<Program title>

jmp start

;data


;code
;IF KEY FOUND THEN IT SHOW 1 AT 000BH MEMORY LOCATION,ELSE IT GIVES 0
start: nop
LXI H,0000H ;STORE COUNT
MOV C,M
INX H ;POINT TO KEY VALUE
MOV B,M
MOV A,B
LOOP: INX H
CMP M
JZ GOTCHA
DCR C
JNZ LOOP
MVI A,00H
STA 000BH
JMP FINISH
GOTCHA: MVI A,00H
INR A
STA 000BH
FINISH: HLT